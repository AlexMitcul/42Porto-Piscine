int	ft_is_prime(int nb)
{
	int	x;
	int	c;

	if (nb <= 1)
		return (0);
	x = 2;
	c = 0;
	while (x < nb / 2)
	{
		if ((nb % x) == 0)
			c++;
		x++;
	}
	if (c == 0)
		return (1);
	return (0);
}
